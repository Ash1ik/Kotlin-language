fun main() {

    // variable can be initiate like (var: data types)

    var score: Int = 10
    var temperature: Double = 90.0
    var israinning: Boolean = false
    var alphabet: Char = 'A'
    var message: String = "Ashik Iqbal"

    println("This is Integer: "+score)
    println("This is Double: "+temperature)
    println("This is Boolean: "+israinning)
    println("This is Char: "+alphabet)
    println("This is String: "+message)

    /*
    output:
        10
        90.0
        false
        A
        Ashik Iqbal
     */
}